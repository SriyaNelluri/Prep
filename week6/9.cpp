class Solution
{
    public:
    //Function to return the level order traversal of a tree.
    vector<int> levelOrder(Node* root)
    {
      //Your code here
      vector<int> res;
       if (root == NULL)  return {0};
    queue<Node *> q;

    q.push(root);
 
    while (q.empty() == false){
        Node *node = q.front();
        res.push_back(node->data);
        q.pop();
        if (node->left != NULL)
            q.push(node->left);
        if (node->right != NULL)
            q.push(node->right);
    }
    return res;
    }
};
